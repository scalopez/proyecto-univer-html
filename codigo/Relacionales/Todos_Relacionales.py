# Uso de realcionales
a = int(input("Ingresa el valor de a: "))
b = int(input("Ingresa el valor de b: "))

# Operador de igualdad (==)
if a == b:
    print(f"{a} es igual a {b}")
else:
    print(f"{a} no es igual a {b}")

# Operador de desigualdad (!=)
if a != b:
    print(f"{a} es diferente de {b}")
else:
    print(f"{a} no es diferente de {b}")

# Operador mayor que (>)
if a > b:
    print(f"{a} es mayor que {b}")
else:
    print(f"{a} no es mayor que {b}")

# Operador menor que (<)
if a < b:
    print(f"{a} es menor que {b}")
else:
    print(f"{a} no es menor que {b}")

# Operador mayor o igual que (>=)
if a >= b:
    print(f"{a} es mayor o igual que {b}")
else:
    print(f"{a} no es mayor o igual que {b}")

# Operador menor o igual que (<=)
if a <= b:
    print(f"{a} es menor o igual que {b}")
else:
    print(f"{a} no es menor o igual que {b}")